public class hw01 {
    public static void main(String[] args){
        /*
        计算下列表达式的结果：
        10/3  10/5  10%2  -10.5%3
         */
        System.out.println(10 / 3);//3
        System.out.println(10 / 5);//2
        System.out.println(10 % 2);//0
        System.out.println(-10.5 % 3);//-1.5

    }
}
